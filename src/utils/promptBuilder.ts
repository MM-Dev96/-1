export function buildSelfEvaluationPrompt(
  focusAreas: string[],
  customNotes: string,
  depth: 'quick' | 'deep',
  appCode: string
): string {
  let focusInstructions = "";
  if (focusAreas.includes('all')) {
     focusInstructions = "التركيز المطلوب بشدة: **التدقيق الشامل للنظام (Full System Audit)**. مراجعة المعمارية، الأمان، الأداء، وهيكلة النظام بالكامل. يجب أن يشمل التدقيق حرفياً كل صغيرة وكبيرة في النظام بما في ذلك: القائمة الجانبية والتنقل، محرك المعاينة الحية، نظام الإشعارات، الإعدادات وتخصيص النظام، خط الإنتاج وتنسيق المهام، المستودع وحفظ البيانات، أداء وكلاء الذكاء الاصطناعي، الأمان، وتجربة المستخدم. قدم اقتراحات تفصيلية لأدق التفاصيل والتحسينات الممكنة.";
  } else {
     const areas = [];
     if (focusAreas.includes('pipeline')) areas.push("خط الإنتاج وتنسيق المهام (Production Pipeline & Orchestrator) - إدارة حالة الوكلاء والأخطاء");
     if (focusAreas.includes('repository')) areas.push("المستودع وحفظ البيانات (Repository & Data Persistence) - حل مشكلة المستودع الوهمي واسترجاع المشاريع");
     if (focusAreas.includes('ux')) areas.push("تجربة المستخدم (UX) - التفاعلية والتعامل مع فترات الانتظار الطويلة");
     if (focusAreas.includes('security')) areas.push("الأمان (Security) - الثغرات وحماية البيانات");
     if (focusAreas.includes('performance')) areas.push("الأداء (Performance) - تحسين سرعة الاستجابة وكفاءة الكود");
     if (focusAreas.includes('sidebar_nav')) areas.push("القائمة الجانبية والتنقل (Sidebar & Navigation) - هيكلة التنقل، تحسين تجربة المستخدم للوصول السريع، وتصميم الواجهة الجانبية");
     if (focusAreas.includes('preview_engine')) areas.push("محرك المعاينة الحية (Live Preview Engine) - دقة عرض المخرجات، سرعة التحميل، طريقة العرض المباشر للكود المولد، والتفاعلية");
     if (focusAreas.includes('notifications')) areas.push("نظام الإشعارات (Notifications System) - تنبيهات حالة المهام، إشعارات الأخطاء، تحسين وصول المعلومة للمستخدم بشكل فوري");
     if (focusAreas.includes('settings')) areas.push("الإعدادات (Settings & Configuration) - تخصيص النظام، إدارة التفضيلات، الخيارات المتاحة للمستخدم");
     if (focusAreas.includes('ai_agents')) areas.push("وكلاء الذكاء الاصطناعي (AI Agents) - جودة المخرجات، دقة التوجيه (Prompting)، تسلسل المهام، وكيفية تواصل الوكلاء مع بعضهم");
     if (focusAreas.includes('agent_integration')) areas.push("التدقيق المعماري والتكامل مع الذكاء الاصطناعي (Agent Integration) - مراجعة توافق معمارية التطبيق الحالية وحجمه وتعقيده مع قدرات وحدود وكيل الذكاء الاصطناعي (أنت) من حيث القدرة على تنفيذ التعديلات وإدارة الملفات المتعددة");
     focusInstructions = "التركيز المطلوب بشدة على المجالات التالية:\n" + areas.map(a => "- " + a).join("\n");
  }

  if (customNotes.trim() !== '') {
     focusInstructions += `\n\n**ملاحظات مخصصة من المستخدم (مهم جداً التركيز عليها):**\n${customNotes}`;
  }

  let depthInstruction = "";
  if (depth === 'deep') {
    depthInstruction = "يجب أن يكون التدقيق مركزاً وعملياً جداً (Actionable Scope). بصفتك مساعد ذكاء اصطناعي (AI Coding Agent)، يجب أن تضع خطة يمكن لوكيل الذكاء الاصطناعي (أنت) تنفيذها في خطوة واحدة أو خطوات قصيرة. **التقرير يجب أن يكون مختصراً جداً (لا يتجاوز 500 كلمة)**، وتجنب الإسهاب أو تقديم اقتراحات ضخمة جداً تتطلب إعادة كتابة النظام بالكامل. ركز فقط على المشاكل التي يمكن حلها فوراً بأكواد برمجية محددة.";
  } else {
    depthInstruction = "يجب أن يكون التدقيق عبارة عن نظرة سريعة وملخص. ركز على المشاكل الأساسية والواضحة جداً وقدم نصائح سريعة وقصيرة لتحسينها بدون تفاصيل معمارية معقدة. التقرير يجب أن يكون قصيراً جداً.";
  }

  return `أنت الآن كبير مهندسي برمجيات (Principal Software Engineer) ومهندس معماري للنظم المؤسسية (Enterprise Systems Architect) خبير في بناء أنظمة سير العمل (Workflow Orchestration) ومنصات الـ SaaS المتقدمة.
قم بمراجعة الكود المصدري لهذا التطبيق (وهو حالياً نموذج مبدئي "Prototype" لمنسق سير عمل هندسي).
الهدف الأساسي هو **ترقية التطبيق من مجرد نموذج شكلي إلى نظام حقيقي متكامل ومتقدم (Feature Development & Architecture)**.

${focusInstructions}

${depthInstruction}

**مهم جداً: تجاهل تماماً الأمور البصرية (الألوان، التصميم، CSS، UI/UX السطحي) إلا إذا كان التركيز على تجربة المستخدم (UX). تركيزك يجب أن يكون بنسبة 100% على تطوير الميزات والهندسة البرمجية للتركيز المحدد.**

**تنبيه حاسم جداً:** ممنوع منعاً باتاً كتابة أي مقدمة أو خاتمة أو تحية أو مدح أو جملة افتتاحية مثل "بصفتي مهندس...". ابدأ إجابتك مباشرة وفوراً من العنوان الأول (## 1. تحليل المشكلة الحالية). أريد التقرير التقني الجاف المباشر فقط.

قدم تقريراً هندسياً احترافياً باللغة العربية منسقاً بـ Markdown يحتوي حصراً على الأقسام التالية:

## 1. تحليل المشكلة الحالية (Current State Analysis)
بناءً على المجالات المحددة (${focusAreas.join(', ')}). اذكر المشاكل الحالية لكل مجال بتفصيل شديد وعميق.

## 2. تطوير ميزات جديدة قوية (Feature Engineering)
الحلول البرمجية المعمارية المقترحة لكل مشكلة، كن دقيقاً جداً واقترح ميزات متقدمة.

## 3. جودة الكود المعمارية (Code Architecture & Performance)
ملاحظات معمارية حول كيفية التنفيذ الفعلي في الكود وإعادة الهيكلة.

## 4. تصميم واجهة المستخدم وملائمة الشاشات (UI/UX & Responsive Design)
تحليل دقيق لتصميم الشاشات الحالي ومشاكل التجاوب (Responsiveness) على مختلف الأجهزة. قدم اقتراحات تفصيلية لتحسين واجهة المستخدم وتجربته.

## 5. المخطط الهيكلي والشكل المرئي (Visual Architecture & Wireframes)
بما أن التطبيقات تُفهم بشكلها المرئي، قم بإنشاء رسم تخطيطي باستخدام (Mermaid.js) يوضح المخطط الهيكلي للتطبيق وتدفق الشاشات (Screen Flow) والعلاقات بينها. يجب أن ترسم مخطط (Graph) أو (Flowchart) احترافي.
بالإضافة إلى ذلك، قدم تخطيطاً شبكياً نصياً (ASCII Wireframe) أو وصفاً بصرياً دقيقاً جداً يسهل تخيل الشكل النهائي للتطبيق ومقاطع الواجهة، لتكون الصورة واضحة للمستخدم الذي يتخيل الواجهة.

## 6. توصيات عاجلة (Urgent Action Items)
خطوات فورية قابلة للتنفيذ لإصلاح المشاكل الحالية.

الكود المصدري للملفات التي تم تضمينها في هذا السياق:
\`\`\`
${appCode}
\`\`\``;
}
